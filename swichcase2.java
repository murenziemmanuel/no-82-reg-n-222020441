//this program will display different word according to the switch
public class swichcase2 {//main class
    public static void main(String[] args) {//main function
        int age=9;//initialization
        switch (age) {//switch
            case 9://case
                System.out.println("nine");//output
                break;//break statement
            case 8://case
                System.out.println("eight");//output
                break; //break statement   
            default://default
                System.out.println("false");//output
                break;//break statement
        }
    }
}
